// Ref: https://github.com/duckdb/duckdb/blob/ff7f24fd8e3128d94371827523dae85ebaf58713/third_party/libpg_query/grammar/keywords/
//
// There are several sets of "reserved" words, and they are joined together in
// the DUCKDB_RESERVED_WORDS constant.

var DUCKDB_RESERVED_WORDS = func() []string {
	var out []string

	for _, words := range [][]string{
		column_name_keywords,
		func_name_keywords,
		reserved_keywords,
		type_name_keywords,
		unreserved_keywords,
	} {
		out = append(out, words...)
	}

	// There are some repeats amongst the separate lists which are removed
	// before returning the final list.
	slices.Sort(out)
	return slices.Compact(out)
}()

